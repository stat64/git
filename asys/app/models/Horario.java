package models;

public class Horario {

	int Nro;
	String docente;
	String Hora;
	String Lunes;
	String Martes;
	String Miercoles;
	String Jueves;
	String Viernes;
	String Sabado;

	public Horario(int Nro, String docente, String Hora, String Lunes,
			String Martes, String Miercoles, String Jueves, String Viernes,
			String Sabado) {
		Nro = this.Nro;
		docente = this.docente;
		Hora = this.Hora;
		Lunes = this.Lunes;
		Martes = this.Martes;
		Miercoles = this.Miercoles;
		Jueves = this.Jueves;
		Viernes = this.Viernes;
		Sabado = this.Sabado;
	}

}
